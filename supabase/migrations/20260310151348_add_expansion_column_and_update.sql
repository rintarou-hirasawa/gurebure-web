BEGIN
  IF NOT EXISTS (
    SELECT 1 FROM information_schema.columns
    WHERE table_name = 'cards' AND column_name = 'expansion'
  ) THEN
    ALTER TABLE cards ADD COLUMN expansion text;
  END IF;
END $$;

